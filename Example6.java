public class Example6
{
   public static void main(String [] args)
   {
     int num1 = 125;
	 System.out.println("input first no.: " + num1);
	 
	 int num2 = 24;
	 System.out.println("input second no.: " + num2);
	 
	 System.out.println(num1 + "+" + num2 + "="+ (num1+num2));
	 System.out.println(num1 + "-" + num2 + "="+ (num1-num2));
	 System.out.println(num1 + "*" + num2 + "="+ (num1*num2));
	 System.out.println(num1 + "/" + num2 + "="+ (num1/num2));
	 System.out.println(num1 + "%" + num2 + "="+ (num1%num2));
   }
}   